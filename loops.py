# bucle for
print("Bucle 1")
for i in [0, 1, 2, 3, 4, 5]:
    print(i)

print("Bucle 2")
cantidadVeces = 10
for i in range(cantidadVeces):
    print(i)

nombres = ["Harry", "Hermione", "Ron"]
for nombre in nombres:
    print(nombre)
print("Longitud:", len(nombres))

cadena = "Hola que tal este es un texto"
for caracter in cadena:
    print(caracter)
